# Khá»Ÿi táº¡o client vÃ  chat session (cached Ä‘á»ƒ duy trÃ¬ phiÃªn)
@st.cache_resource
def get_chat_session(api_key):
    """Khá»Ÿi táº¡o hoáº·c tráº£ vá» chat session Ä‘Ã£ lÆ°u cache."""
    try:
        if not api_key:
            # Tráº£ vá» None Ä‘á»ƒ bÃ¡o hiá»‡u lá»—i API Key
            return None, "Lá»—i: KhÃ´ng tÃ¬m tháº¥y KhÃ³a API. Chatbot yÃªu cáº§u KhÃ³a 'GEMINI_API_KEY'."
            
        client = genai.Client(api_key=api_key)
        
        # ÄÃƒ Sá»¬A Lá»–I: client.chats.create() khÃ´ng cÃ²n tham sá»‘ system_instruction
        chat_session = client.chats.create(
            model='gemini-2.5-flash'
        )
        return chat_session, None

    except Exception as e:
        # Thay Ä‘á»•i thÃ´ng bÃ¡o lá»—i Ä‘á»ƒ rÃµ rÃ ng hÆ¡n náº¿u lá»—i khÃ´ng pháº£i do API Key
        return None, f"Lá»—i khá»Ÿi táº¡o Chat: {e}"

def chat_interface():
    """Hiá»ƒn thá»‹ giao diá»‡n chatbot trong sidebar."""
    st.sidebar.title("ğŸ’¬ Trá»£ lÃ½ TÃ i chÃ­nh Gemini")
    st.sidebar.markdown("Há»i Gemini vá» cÃ¡c khÃ¡i niá»‡m tÃ i chÃ­nh, káº¿ toÃ¡n hoáº·c báº¥t ká»³ cÃ¢u há»i nÃ o khÃ¡c.")
    
    api_key = st.secrets.get("GEMINI_API_KEY")

    chat_session, error_message = get_chat_session(api_key)
    
    if error_message:
        # Hiá»ƒn thá»‹ lá»—i API Key trong sidebar
        st.sidebar.error(error_message)
        return

    # Khá»Ÿi táº¡o lá»‹ch sá»­ chat trong Streamlit session state
    if "messages" not in st.session_state:
        st.session_state.messages = []
        # ThÃªm tin nháº¯n chÃ o má»«ng máº·c Ä‘á»‹nh, thiáº¿t láº­p persona táº¡i Ä‘Ã¢y
        st.session_state.messages.append({
            "role": "model", 
            "content": "ChÃ o báº¡n! TÃ´i lÃ  trá»£ lÃ½ Gemini, chuyÃªn tÆ° váº¥n vá» tÃ i chÃ­nh, káº¿ toÃ¡n vÃ  phÃ¢n tÃ­ch doanh nghiá»‡p. Báº¡n cÃ³ cÃ¢u há»i gÃ¬ khÃ´ng?"
        })

    # Hiá»ƒn thá»‹ lá»‹ch sá»­ chat
    for message in st.session_state.messages:
        with st.sidebar.chat_message(message["role"]):
            st.markdown(message["content"])

    # Xá»­ lÃ½ input tá»« ngÆ°á»i dÃ¹ng
    if prompt := st.sidebar.chat_input("Nháº­p cÃ¢u há»i cá»§a báº¡n..."):
        # 1. LÆ°u vÃ  hiá»ƒn thá»‹ tin nháº¯n ngÆ°á»i dÃ¹ng
        st.session_state.messages.append({"role": "user", "content": prompt})
        with st.sidebar.chat_message("user"):
            st.markdown(prompt)

        # 2. Gá»­i tin nháº¯n Ä‘áº¿n Gemini vÃ  hiá»ƒn thá»‹ káº¿t quáº£
        with st.sidebar.chat_message("model"):
            with st.spinner("Äang chá» pháº£n há»“i tá»« Gemini..."):
                try:
                    # Gá»­i tin nháº¯n má»›i nháº¥t. Chat session tá»± quáº£n lÃ½ lá»‹ch sá»­.
                    response = chat_session.send_message(prompt)
                    ai_response = response.text
                    
                    st.markdown(ai_response)
                    st.session_state.messages.append({"role": "model", "content": ai_response})

                except APIError as e:
                    error_msg = f"Lá»—i API: {e}. Vui lÃ²ng kiá»ƒm tra láº¡i KhÃ³a API."
                    st.error(error_msg)
                    st.session_state.messages.append({"role": "model", "content": error_msg})
                except Exception as e:
                    error_msg = f"Lá»—i khÃ´ng xÃ¡c Ä‘á»‹nh: {e}"
                    st.error(error_msg)
                    st.session_state.messages.append({"role": "model", "content": error_msg})


